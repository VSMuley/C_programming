#include <stdio.h>

int main() {
  
 char character = 'z';  //Strings in C
 float f = 3.4;
 double df = 56.5;
 
 printf("%c\n", character); 
 printf("%d\n", character); //Integer equivalent value of the string
 printf("%f\n", f);
 printf("%.3f\n", f);
 printf("%lf\n", df);
  
  return 0; 
}
